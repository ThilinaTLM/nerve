import { mkdir, mkdtemp, readFile, rm, writeFile } from "node:fs/promises";
import { tmpdir } from "node:os";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import pngToIco from "png-to-ico";
import sharp from "sharp";

/**
 * Generate brand assets from canonical SVG sources.
 *
 * Run from repo root: node scripts/generate-brand-assets.mjs
 * Desktop-only (during desktop-shell build): node scripts/generate-brand-assets.mjs --desktop
 *
 * Sources: assets/brand/*.svg
 * Outputs:
 *   - Desktop shell: build/icon.svg, build/icons, build/windows, build/tray
 *   - Browser apps: public/{favicon.svg,favicon-{16,32}.png,apple-touch-icon.png,...}
 */

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = dirname(scriptDir);
const brandDir = join(repoRoot, "assets", "brand");
const desktopDir = join(repoRoot, "packages", "desktop-shell");
const desktopOutputDir = join(desktopDir, "build");
const desktopIconsDir = join(desktopOutputDir, "icons");
const desktopWindowsDir = join(desktopOutputDir, "windows");
const desktopTrayDir = join(desktopOutputDir, "tray");
const desktopLinuxTrayDir = join(desktopTrayDir, "linux");
const desktopWindowsTrayDir = join(desktopTrayDir, "windows");
const desktopMacosTrayDir = join(desktopTrayDir, "macos");
const desktopOnly = process.argv.includes("--desktop");

const appIconSizes = [16, 20, 24, 32, 40, 48, 64, 128, 256, 512, 1024];
const windowsAppIconSizes = [16, 20, 24, 32, 40, 48, 64, 256];
const windowsTrayIconSizes = [16, 20, 24, 32];
const browserTargets = [
  join(repoRoot, "packages", "workbench-app", "public"),
  join(repoRoot, "packages", "sandbox-manager-app", "public"),
];

const appIconSvg = await readFile(join(brandDir, "nerve-app-icon.svg"));
const fullMarkSvg = await readFile(join(brandDir, "nerve-mark.svg"), "utf8");
const smallMarkSvg = await readFile(
  join(brandDir, "nerve-mark-small.svg"),
  "utf8",
);
const smallAppIconSvg = Buffer.from(
  appIconWithMark(extractMarkGroup(smallMarkSvg)),
);

await generateDesktopAssets();
if (!desktopOnly) await generateBrowserAssets();

async function generateDesktopAssets() {
  await rm(desktopOutputDir, { recursive: true, force: true });
  await Promise.all([
    mkdir(desktopIconsDir, { recursive: true }),
    mkdir(desktopWindowsDir, { recursive: true }),
    mkdir(desktopLinuxTrayDir, { recursive: true }),
    mkdir(desktopWindowsTrayDir, { recursive: true }),
    mkdir(desktopMacosTrayDir, { recursive: true }),
  ]);

  await Promise.all(
    appIconSizes.map(async (size) => {
      const source = size <= 32 ? smallAppIconSvg : appIconSvg;
      await writePng(
        source,
        join(desktopIconsDir, `${size}x${size}.png`),
        size,
      );
    }),
  );

  await Promise.all([
    writeFile(join(desktopOutputDir, "icon.svg"), appIconSvg),
    writeIcoFromPaths(
      windowsAppIconSizes.map((size) =>
        join(desktopIconsDir, `${size}x${size}.png`),
      ),
      join(desktopWindowsDir, "app.ico"),
    ),
    writeLinuxTrayVariant("tray-light", "#0c0a09", "#fbfaf9"),
    writeLinuxTrayVariant("tray-dark", "#fbfaf9", "#0c0a09"),
    writeWindowsTrayVariant("tray-light", "#0c0a09", "#fbfaf9"),
    writeWindowsTrayVariant("tray-dark", "#fbfaf9", "#0c0a09"),
    writeMacosTrayTemplate(),
  ]);

  await writeFile(
    join(desktopOutputDir, "README.md"),
    [
      "Generated by scripts/generate-brand-assets.mjs from assets/brand/*.svg.",
      "",
      "- icon.svg: canonical electron-builder application icon source",
      "- icons/: Linux application PNG set",
      "- windows/app.ico: Windows runtime/development application icon",
      "- tray/linux/: themed Linux tray PNGs",
      "- tray/windows/: themed multi-resolution Windows tray ICOs",
      "- tray/macos/: macOS template image pair",
      "",
      "Do not edit generated assets by hand.",
      "",
    ].join("\n"),
  );
}

async function writeLinuxTrayVariant(name, backgroundColor, markColor) {
  const source = Buffer.from(
    trayIconWithMark(backgroundColor, markColor, false),
  );
  await Promise.all([
    writePng(source, join(desktopLinuxTrayDir, `${name}.png`), 32),
    writePng(source, join(desktopLinuxTrayDir, `${name}@2x.png`), 64),
  ]);
}

async function writeWindowsTrayVariant(name, backgroundColor, markColor) {
  const source = Buffer.from(
    trayIconWithMark(backgroundColor, markColor, false),
  );
  await writeIcoFromSource(
    source,
    windowsTrayIconSizes,
    join(desktopWindowsTrayDir, `${name}.ico`),
  );
}

async function writeMacosTrayTemplate() {
  const source = Buffer.from(trayIconWithMark("#000000", "#000000", true));
  await Promise.all([
    writePng(source, join(desktopMacosTrayDir, "nerveTemplate.png"), 16),
    writePng(source, join(desktopMacosTrayDir, "nerveTemplate@2x.png"), 32),
  ]);
}

async function writeIcoFromSource(source, sizes, destination) {
  const temporaryDir = await mkdtemp(join(tmpdir(), "nerve-icons-"));
  try {
    const paths = sizes.map((size) =>
      join(temporaryDir, `${size}x${size}.png`),
    );
    await Promise.all(
      paths.map((path, index) => writePng(source, path, sizes[index])),
    );
    await writeIcoFromPaths(paths, destination);
  } finally {
    await rm(temporaryDir, { recursive: true, force: true });
  }
}

async function writeIcoFromPaths(paths, destination) {
  const ico = await pngToIco(paths);
  await mkdir(dirname(destination), { recursive: true });
  await writeFile(destination, ico);
}

async function generateBrowserAssets() {
  const faviconSvg = Buffer.from(themeAwareFavicon(fullMarkSvg));
  const favicon16 = await renderPng(
    Buffer.from(setMarkColor(smallMarkSvg, "#0c0a09")),
    16,
  );
  const favicon32 = await renderPng(
    Buffer.from(setMarkColor(fullMarkSvg, "#0c0a09")),
    32,
  );
  const appleTouchIcon = await renderPng(appIconSvg, 180, true);

  await Promise.all(
    browserTargets.map(async (publicDir) => {
      await mkdir(publicDir, { recursive: true });
      await Promise.all([
        writeFile(join(publicDir, "favicon.svg"), faviconSvg),
        writeFile(join(publicDir, "favicon-16.png"), favicon16),
        writeFile(join(publicDir, "favicon-32.png"), favicon32),
        writeFile(join(publicDir, "apple-touch-icon.png"), appleTouchIcon),
      ]);
    }),
  );

  const workbenchPublicDir = browserTargets[0];
  await Promise.all([
    writePng(appIconSvg, join(workbenchPublicDir, "pwa-192x192.png"), 192),
    writePng(appIconSvg, join(workbenchPublicDir, "pwa-512x512.png"), 512),
    writePng(
      appIconSvg,
      join(workbenchPublicDir, "pwa-maskable-512x512.png"),
      512,
      true,
    ),
  ]);
}

function extractMarkGroup(svg) {
  const match = svg.match(/<g id="nerve-mark-small"[\s\S]*?<\/g>/);
  if (!match) throw new Error("Could not find the small Nerve mark group");
  return match[0].replace(' id="nerve-mark-small"', "");
}

function appIconWithMark(markGroup) {
  return `<svg width="512" height="512" viewBox="0 0 512 512" fill="none" xmlns="http://www.w3.org/2000/svg">
  <rect x="32" y="32" width="448" height="448" rx="120" fill="#0C0A09" />
  ${setMarkColor(markGroup, "#FBFAF9")}
</svg>\n`;
}

function trayIconWithMark(backgroundColor, markColor, template) {
  const mark = setMarkColor(extractMarkGroup(smallMarkSvg), markColor);
  const transformedMark = `<g transform="translate(256 256) scale(1.1) translate(-256 -256)">${mark}</g>`;

  if (template) {
    return `<svg width="512" height="512" viewBox="0 0 512 512" xmlns="http://www.w3.org/2000/svg">
  <mask id="tray-mask">
    <rect width="512" height="512" fill="black" />
    <rect x="64" y="64" width="384" height="384" rx="96" fill="white" />
    ${setMarkColor(transformedMark, "black")}
  </mask>
  <rect width="512" height="512" fill="${backgroundColor}" mask="url(#tray-mask)" />
</svg>\n`;
  }

  return `<svg width="512" height="512" viewBox="0 0 512 512" fill="none" xmlns="http://www.w3.org/2000/svg">
  <rect x="64" y="64" width="384" height="384" rx="96" fill="${backgroundColor}" />
  ${transformedMark}
</svg>\n`;
}

function setMarkColor(svg, color) {
  return svg.replaceAll("currentColor", color);
}

function themeAwareFavicon(svg) {
  return svg.replace(
    /(<svg[^>]*>)/,
    `$1
  <style>
    :root { color: #0c0a09; }
    @media (prefers-color-scheme: dark) {
      :root { color: #fbfaf9; }
    }
  </style>`,
  );
}

async function writePng(source, destination, size, flatten = false) {
  await mkdir(dirname(destination), { recursive: true });
  await render(source, size, flatten).toFile(destination);
}

async function renderPng(source, size, flatten = false) {
  return render(source, size, flatten).toBuffer();
}

function render(source, size, flatten) {
  let pipeline = sharp(source).resize(size, size, { fit: "contain" });
  if (flatten) pipeline = pipeline.flatten({ background: "#0c0a09" });
  return pipeline.png({ compressionLevel: 9 });
}
